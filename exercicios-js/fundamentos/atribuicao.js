// Atribuição '='
const a = 7;
let b = 3;

// Atribuição aditiva'+='
b += a;
console.log(b);

// Atribuição subtrativa '-'
b -= 4;
console.log(b);

// Atribuição multiplicativa '*'
b *= 2;
console.log(b);

// Atribuição divisiva '/'
b /= 2;
console.log(b);

// Atribuição modular '%'
b %= 2;
console.log(b);

